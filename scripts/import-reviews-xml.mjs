/**
 * WordPress WXR → lib/reviews-data.ts importer
 *
 * Usage:
 *   node scripts/import-reviews-xml.mjs
 */

import { XMLParser } from "fast-xml-parser";
import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const XML_PATH = join(__dirname, "healthpointejacksonville.reviews.xml");
const OUTPUT_PATH = join(ROOT, "lib", "reviews-data.ts");

const parser = new XMLParser({
  ignoreAttributes: false,
  attributeNamePrefix: "@_",
  cdataPropName: "__cdata",
  isArray: (name) => ["item", "wp:postmeta", "category"].includes(name),
  parseTagValue: false,
  trimValues: true,
});

console.log("Reading XML…");
const xml = readFileSync(XML_PATH, "utf-8");
const parsed = parser.parse(xml);
const items = parsed?.rss?.channel?.item ?? [];
console.log(`Found ${items.length} total items`);

const reviews = [];

for (const item of items) {
  const postType = cdata(item["wp:post_type"]);
  const status = cdata(item["wp:status"]);
  if (postType !== "testimonials" || status !== "publish") continue;

  const name = decodeHtmlEntities(cdata(item.title) ?? "");
  const slug = cdata(item["wp:post_name"]) ?? "";
  const rawDate = cdata(item["wp:post_date"]) ?? "";
  const date = rawDate.slice(0, 10);
  const quote = stripHtml(cdata(item["content:encoded"]) ?? "").trim();

  const categoryEls = Array.isArray(item.category)
    ? item.category
    : item.category ? [item.category] : [];

  const categories = [];
  for (const el of categoryEls) {
    const domain = el["@_domain"];
    const label = cdata(el) || el["#text"] || el;
    if (typeof label !== "string") continue;
    if (domain === "testimonial_categoriet") categories.push(decodeHtmlEntities(label));
  }
  if (categories.length === 0) categories.push("General");

  if (!quote) continue;

  reviews.push({ slug, name, quote, date, categories });
}

reviews.sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
console.log(`Found ${reviews.length} published testimonials`);

const lines = [
  `import type { Review } from "./reviews";`,
  ``,
  `// Generated by scripts/import-reviews-xml.mjs — ${new Date().toISOString().slice(0, 10)}`,
  `// ${reviews.length} reviews imported from WordPress export`,
  `export const REVIEWS_DATA: Review[] = [`,
];

for (const r of reviews) {
  lines.push(`  {`);
  lines.push(`    slug: ${JSON.stringify(r.slug)},`);
  lines.push(`    name: ${JSON.stringify(r.name)},`);
  lines.push(`    quote: ${JSON.stringify(r.quote)},`);
  lines.push(`    date: ${JSON.stringify(r.date)},`);
  lines.push(`    categories: ${JSON.stringify(r.categories)},`);
  lines.push(`  },`);
}

lines.push(`];`);
lines.push(``);

writeFileSync(OUTPUT_PATH, lines.join("\n"), "utf-8");
console.log(`✅ Done — ${reviews.length} reviews written to lib/reviews-data.ts`);

function cdata(node) {
  if (node == null) return null;
  if (typeof node === "string") return node;
  if (typeof node === "object") {
    if (node.__cdata != null) return String(node.__cdata);
    if (node["#text"] != null) return String(node["#text"]);
  }
  return String(node);
}

function stripHtml(html) {
  return decodeHtmlEntities(
    (html || "")
      .replace(/<[^>]+>/g, " ")
      .replace(/\s{2,}/g, " ")
  ).trim();
}

function decodeHtmlEntities(str) {
  return (str || "")
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, '"')
    .replace(/&#039;/g, "'")
    .replace(/&apos;/g, "'");
}
